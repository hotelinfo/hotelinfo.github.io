            function ScaleSlider(slider) {
                var parentWidth = slider.$Elmt.parentNode.clientWidth;
                if (parentWidth)
                    slider.$ScaleWidth(Math.max(Math.min(parentWidth, 800), 300));
                else
                    window.setTimeout(ScaleSlider(slider), 30);
            }
            
