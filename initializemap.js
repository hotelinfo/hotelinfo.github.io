function initializemap(canvasid, slat, slng, stitle, surl) {
  var mapOptions = {zoom: 14,center: {lat: slat, lng: slng},zoomControl: true,zoomControlOptions: {style: google.maps.ZoomControlStyle.DEFAULT,},disableDoubleClickZoom: true,mapTypeControl: true,mapTypeControlOptions: {style: google.maps.MapTypeControlStyle.DROPDOWN_MENU,},scaleControl: true,scrollwheel: true,panControl: true,streetViewControl: true,draggable : true,overviewMapControl: true,overviewMapControlOptions: {opened: false,},mapTypeId: google.maps.MapTypeId.SATELLITE};
  var map = new google.maps.Map(document.getElementById(canvasid),mapOptions);
  var marker = new google.maps.Marker({url: surl,position: {lat: slat, lng: slng},map: map,title: stitle});
  google.maps.event.addListener(marker, 'click', function() {window.location.href = marker.url;});
  return map;
}

