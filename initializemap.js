		function getFirstChildWithTagName( element, tagName ) {
      for ( var i = 0; i < element.childNodes.length; i++ ) {
        if ( element.childNodes[i].nodeName == tagName ) return element.childNodes[i];
      }
    }

    function getHash( url ) {
      var hashPos = url.lastIndexOf ( '#' );
      return url.substring( hashPos + 1 );
    }


    var hotels = new Array();   
    
    var contentDivsArr = new Array();
   var tabLinksArr = new Array();

    function init(hotelid) {
    	
    	var contentDivs = new Array();
    	var tabLinks = new Array();
    	
      contentDivsArr[hotelid]=contentDivs;
      tabLinksArr[hotelid]=tabLinks;
      

			var sname='tabs'+hotelid;
			var tabselement=document.getElementById(sname);

      // Grab the tab links and content divs from the page
      var tabListItems = tabselement.childNodes;
      for ( var i = 0; i < tabListItems.length; i++ ) {
        if ( tabListItems[i].nodeName == "LI" ) {
          var tabLink = getFirstChildWithTagName( tabListItems[i], 'A' );
          var id = getHash( tabLink.getAttribute('href') );
          
          tabLinks[id] = tabLink;
          var el=document.getElementById( id );     
          contentDivs[id] = el;
        }
      }
   
      // Assign onclick events to the tab links, and
      // highlight the first tab
      var i = 0;

      for ( var id in tabLinks ) {
        tabLinks[id].onclick = showTab;
        tabLinks[id].onfocus = function() { this.blur();};
        if ( i == 0 ) tabLinks[id].className = 'selected';
        i++;
      }

      // Hide all content divs except the first
      var i = 0;

      for ( var id in contentDivs ) {
        if ( i != 0 ) contentDivs[id].className = 'tabContent hide';
        i++;
      }
       //alert(contentDivsArr.length);
    }

    function showTab(e) {
    	//alert('gfdg');
    	e = e || window.event;
      e = e.target || e.srcElement;
    
      hotelid=e.getAttribute('hotelid');
    
    //if (e.nodeName === 'BUTTON') {
      //  alert(e.nodeName);
    //}
      var selectedId = getHash( this.getAttribute('href') );
      
      
      var sname='tabs'+hotelid;
			var tabselement=document.getElementById(sname);

      
      
      
      
      var contentDivs=contentDivsArr[hotelid];
      var tabLinks=tabLinksArr[hotelid];
      
      // Highlight the selected tab, and dim all others.
      // Also show the selected content div, and hide all others.
      for ( var id in contentDivs ) {
      	
        if ( id == selectedId ) {
          tabLinks[id].className = 'selected';
          contentDivs[id].className = 'tabContent';
        } else {
          tabLinks[id].className = '';
          contentDivs[id].className = 'tabContent hide';
        }
      }
      
      //alert(hotels[0].hotelname);
      if ( selectedId.indexOf("map")==0 ) {
      		var hotel=hotels[hotelid];
      		var map=hotel.map;
      		var lat=hotel.lat;
      		var lng=hotel.lng;
      		google.maps.event.trigger(map, "resize");
      		map.setCenter(new google.maps.LatLng(lat,lng));
      		}

      // Stop the browser following the link
      return false;
    }
  
